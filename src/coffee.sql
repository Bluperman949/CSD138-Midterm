-- Analytical Query with ROLLUP
CREATE OR REPLACE VIEW vw_total_price_by_category_subcategory AS
SELECT 
    c.category_name,
    s.subcategory_name,
    ROUND(SUM(i.price), 2) AS total_price
FROM 
    Item i
    JOIN Subcategory s ON i.subcategory_id = s.subcategory_id
    JOIN Category c ON s.category_id = c.category_id
WHERE 
    i.price > 250
GROUP BY 
    c.category_name, s.subcategory_name WITH ROLLUP
HAVING 
    SUM(i.price) > 1000
ORDER BY 
    CASE WHEN c.category_name IS NULL THEN 2
         WHEN s.subcategory_name IS NULL THEN 1
         ELSE 0 END,
    c.category_name,
    s.subcategory_name;

