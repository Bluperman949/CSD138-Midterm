drop function if exists fn_calculate_order_total;

delimiter //

create function fn_calculate_order_total(p_order_id int)
returns decimal(10,2)
deterministic
reads sql data
begin
    declare v_amount decimal(10,2);
    declare v_tax_percent decimal(5,2);
    declare v_total decimal(10,2);

    select amount, tax_percent
      into v_amount, v_tax_percent
      from orders
     where order_id = p_order_id;

    if v_tax_percent is null then
        set v_total = v_amount;
    else
        set v_total = v_amount + round(v_amount * v_tax_percent / 100, 2);
    end if;

    return v_total;
end //

delimiter ;

select order_id, fn_calculate_order_total(order_id) as order_total
  from orders;

